const contractAddress = '0x390Dc1BCf3d79120d1f80E36454B8F7d5b3E8865'; // Use the deployed contract address

let web3, contract, accounts;

async function loadWeb3() {
    if (window.ethereum) {
        window.web3 = new Web3(window.ethereum);
        await window.ethereum.enable();
    } else if (window.web3) {
        window.web3 = new Web3(window.web3.currentProvider);
    } else {
        window.alert('Non-Ethereum browser detected. Please install MetaMask.');
    }
}

async function loadContract() {
    web3 = window.web3;
    accounts = await web3.eth.getAccounts();
    contract = new web3.eth.Contract(contractABI, contractAddress);
}

async function mintTokens() {
    const mintAmount = document.getElementById('mintAmount').value;
    const mintAmountInt = parseInt(mintAmount);

    if (mintAmountInt <= 0) {
        alert('You must mint at least 1 token.');
        return;
    }

    const price = await contract.methods.PRICE().call();
    const totalPrice = BigInt(price) * BigInt(mintAmountInt);
    const gasPrice = await web3.eth.getGasPrice();
    const gasPriceCustom = BigInt(gasPrice) * BigInt(1.1); // Increase gas price by 10%

    contract.methods
        .mint(mintAmountInt)
        .send({ from: accounts[0], value: totalPrice, gasPrice: gasPriceCustom })
        .on('transactionHash', (hash) => {
            document.getElementById('statusMessage').innerHTML = 'Transaction submitted, waiting for confirmation...';
        })
        .on('confirmation', (confirmationNumber, receipt) => {
            document.getElementById('statusMessage').innerHTML = 'Transaction confirmed, NFT(s) minted!';
        })
        .on('error', (error) => {
            document.getElementById('statusMessage').innerHTML = 'Transaction error: ' + error.message;
        });
}

async function initApp() {
    await loadWeb3();
    await loadContract();
}

window.addEventListener('load', initApp);

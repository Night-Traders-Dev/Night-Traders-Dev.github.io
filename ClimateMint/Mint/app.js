let web3;
let contract;
let userAddress;
let gasPrice;

async function loadWeb3() {
    if (window.ethereum) {
        window.web3 = new Web3(window.ethereum);
        await window.ethereum.request({ method: "eth_requestAccounts" });
    } else if (window.web3) {
        window.web3 = new Web3(window.web3.currentProvider);
    } else {
        alert("Non-Ethereum browser detected. You should consider installing MetaMask.");
    }
}

async function loadContract() {
    web3 = window.web3;
    contract = new web3.eth.Contract(ABI, CONTRACT_ADDRESS);
    const accounts = await web3.eth.getAccounts();
    userAddress = accounts[0];
    gasPrice = await web3.eth.getGasPrice();
}

async function mintTokens() {
    const mintCount = document.getElementById("mintCount").value;
    const mintPrice = await contract.methods.PRICE().call();
    const totalCost = web3.utils.toBN(mintPrice).mul(web3.utils.toBN(mintCount));

    contract.methods
        .mint(mintCount)
        .send({ from: userAddress, value: totalCost, gasPrice })
        .on("transactionHash", (hash) => {
            console.log("Transaction hash:", hash);
        })
        .on("confirmation", (confirmationNumber, receipt) => {
            console.log("Transaction confirmed:", confirmationNumber, receipt);
        })
        .on("error", (error) => {
            console.error("Transaction error:", error);
        });
}

async function initApp() {
    await loadWeb3();
    await loadContract();
}

document.getElementById("mintButton").addEventListener("click", mintTokens);
window.addEventListener("load", initApp);

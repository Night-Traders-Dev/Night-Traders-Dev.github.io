// Connect to MetaMask
async function connect() {
    if (window.ethereum) {
        try {
            const accounts = await window.ethereum.request({ method: "eth_requestAccounts" });
            showGameControls();
            return accounts[0];
        } catch (err) {
            console.error("User denied account access");
            return null;
        }
    } else {
        alert("Please install MetaMask!");
        return null;
    }
}


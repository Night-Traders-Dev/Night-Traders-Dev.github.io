// Connect to Metamask
async function connect() {
  if (window.ethereum) {
    try {
      await window.ethereum.request({ method: "eth_requestAccounts" });
      return true;
    } catch (err) {
      console.error("User denied account access");
      return false;
    }
  } else {
    alert("Please install MetaMask!");
    return false;
  }
}

